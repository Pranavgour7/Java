public class CWH_16_conditionals {
    public static void main(String[] args) {
        //If & else Conditioning
        int age = 18;
        if (age>18){
            System.out.println("Yes you can drive");
        }
        else {
            System.out.println("No you cannot drive");
        }
            //!= means not equal to
            int age2 = 18;
            boolean cond = (age2==18);
            if (cond){
                System.out.println("Yes you can drive");
            }
            else {
                System.out.println("No you cannot drive");
        }
    }
}
