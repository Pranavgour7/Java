public class Cwh_22_ch5_dowhile {
    public static void main(String[] args) {
        int a = 0;
        while (a<5){
            System.out.println(a);
            a++;
        }

        // using do while

        int b =10;
        do {
            System.out.println(b);
            b++;
        }while (b<5);

        //

        int v =1;
        do{
            System.out.println(v);
            v++;
        }while(v<=45);
    }
}
